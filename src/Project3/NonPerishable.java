package Project3;
import java.util.*;
/**
 *
 * @author fireb
 */
public class NonPerishable {
    
    private int cannede; 
    private int oats; 
    private int pasta; 

    public NonPerishable(int cannede, int oats, int pasta) {
        this.cannede = cannede;
        this.oats = oats;
        this.pasta = pasta;
    }

    public int getCannede() {
        return cannede;
    }

    public void setCannede(int cannede) {
        this.cannede = cannede;
    }

    public int getOats() {
        return oats;
    }

    public void setOats(int oats) {
        this.oats = oats;
    }

    public int getPasta() {
        return pasta;
    }

    public void setPasta(int pasta) {
        this.pasta = pasta;
    }
    
    
    
}
